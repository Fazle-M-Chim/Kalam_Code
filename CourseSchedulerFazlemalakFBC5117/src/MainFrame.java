    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String selectedStudent1 = (String) DisplayScheduleStudentComboBox.getSelectedItem();
        String[] studentParts = selectedStudent1.split(", ");
        String studentID1 = studentParts[1];
        String inputString = studentID1;
        int spaceIndex = inputString.indexOf(' ');
        String resultStudentID1 = spaceIndex != -1 ? inputString.substring(spaceIndex + 1) : "";
        
        ArrayList<ScheduleEntry> schedule = ScheduleQueries.getScheduleByStudent(currentSemester, resultStudentID1);
        DefaultTableModel currTableMod = (DefaultTableModel)displayScheduleTable.getModel();
        currTableMod.setNumRows(0);
        Object[] rowData = new Object[3];
        for (ScheduleEntry s : schedule)
        {
            rowData[0] = s.getCourseCode();
            rowData[1] = s.getStatus().equals("w") ? "Waitlisted" : "Scheduled";
            currTableMod.addRow(rowData);
        }
    }//GEN-LAST:event_jButton6ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here
        String status = "";
        String selectedStudent = (String) ScheduleStudentComboBox.getSelectedItem();
        String[] studentParts = selectedStudent.split(", ");
        String studentID = studentParts[1];
        String inputString = studentID;
        int spaceIndex = inputString.indexOf(' ');
        String resultStudentID = spaceIndex != -1 ? inputString.substring(spaceIndex + 1) : "";
   
        

        //int x = CourseQueries.getCourseSeats(currentSemester,  (String)selectCourseComboBox.getSelectedItem());
        if (ClassQueries.getClassSeats(currentSemester, (String)selectCourseComboBox.getSelectedItem()) > ScheduleQueries.getScheduledStudentCount(currentSemester, (String)selectCourseComboBox.getSelectedItem()))
        {
            status = "s";
            scheduleCourseOutput.setText(ScheduleStudentComboBox.getSelectedItem() + " has been scheduled for " + (String)selectCourseComboBox.getSelectedItem());
        }
        else
        {
            status = "w";
            scheduleCourseOutput.setText(ScheduleStudentComboBox.getSelectedItem() + " has been waitlisted for " + (String)selectCourseComboBox.getSelectedItem());
        }
        ScheduleEntry newEntry = new ScheduleEntry(currentSemester, (String)selectCourseComboBox.getSelectedItem(), resultStudentID, status, new java.sql.Timestamp(System.currentTimeMillis()));
        ScheduleQueries.addScheduleEntry(newEntry);
              

    }//GEN-LAST:event_jButton5ActionPerformed

